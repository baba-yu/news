  // Loads docs/data/glossary.json once at boot. Active rows with a
  // filled `one_liner_eli14` get registered into state.glossaryMap +
  // state.glossaryPattern. annotateGlossary() then wraps the first
  // occurrence of each term inside any rendered markdown body in
  // `<abbr title="…">…</abbr>` so the dashboard's hover behavior
  // works on every panel.
  async function loadGlossary() {
    try {
      const r = await fetch(`${LIVE_DATA_DIR}/glossary.json`, { cache: "no-store" });
      if (!r.ok) return;
      const data = await r.json();
      const terms = (data && data.terms) || [];
      const map = new Map();
      for (const e of terms) {
        if (!e || !e.one_liner_eli14) continue;
        const all = [e.term, ...(Array.isArray(e.aliases) ? e.aliases : [])];
        for (const t of all) {
          if (!t || typeof t !== "string") continue;
          const key = t.toLowerCase();
          if (!map.has(key)) map.set(key, e);
        }
      }
      if (!map.size) return;
      const tokens = [...map.keys()].sort((a, b) => b.length - a.length);
      const escaped = tokens.map((t) => t.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"));
      state.glossaryMap = map;
      // Lookbehind avoids matching mid-word (e.g. "MCPro" should
      // not match the MCP term). Modern browsers only — Safari 16.3
      // and older silently lose hover, no other regression.
      state.glossaryPattern = new RegExp(
        "(?<![A-Za-z0-9_])(" + escaped.join("|") + ")(?![A-Za-z0-9_])",
        "gi"
      );
    } catch (_) { /* glossary is optional; never block boot */ }
  }

  // Walk a rendered HTML fragment's text nodes and wrap the first
  // occurrence of each glossary term with an `<abbr>`. Skips text
  // inside <a>/<abbr>/<code>/<pre>/<script>/<style> so we don't
  // double-wrap or break URLs.
  function annotateGlossary(html) {
    if (!html) return html;
    if (!state.glossaryMap || !state.glossaryPattern) return html;
    const tmp = document.createElement("div");
    tmp.innerHTML = html;
    const walker = document.createTreeWalker(tmp, NodeFilter.SHOW_TEXT, null);
    const seen = new Set();
    const targets = [];
    let n;
    while ((n = walker.nextNode())) {
      let p = n.parentNode;
      let skip = false;
      while (p && p !== tmp) {
        if (p.nodeType === 1 && /^(a|abbr|code|pre|script|style)$/i.test(p.tagName)) {
          skip = true; break;
        }
        p = p.parentNode;
      }
      if (!skip) targets.push(n);
    }
    for (const node of targets) {
      const text = node.textContent;
      // Fresh regex per node so lastIndex doesn't carry over.
      const pattern = new RegExp(state.glossaryPattern.source, state.glossaryPattern.flags);
      let lastIdx = 0;
      const frag = document.createDocumentFragment();
      let any = false;
      let m;
      while ((m = pattern.exec(text)) !== null) {
        const matched = m[1] || m[0];
        const key = matched.toLowerCase();
        if (seen.has(key)) continue;
        const entry = state.glossaryMap.get(key);
        if (!entry) continue;
        seen.add(key);
        if (m.index > lastIdx) {
          frag.appendChild(document.createTextNode(text.slice(lastIdx, m.index)));
        }
        const abbr = document.createElement("abbr");
        // Per-locale lookup with EN fallback. Export side already
        // fans NULLs out to EN so entry.eli14[loc] / entry.why[loc]
        // never returns null/undefined for a registered active term.
        // Older builds that predate the fan-out fall back to the
        // legacy flat fields.
        const loc = state.locale || "en";
        const eli = (entry.eli14 && (entry.eli14[loc] || entry.eli14.en))
          || entry.one_liner_eli14
          || "";
        const why = (entry.why && (entry.why[loc] || entry.why.en))
          || entry.why_it_matters
          || "";
        abbr.title = why ? `${eli} — ${why}` : eli;
        abbr.textContent = matched;
        frag.appendChild(abbr);
        lastIdx = m.index + matched.length;
        any = true;
      }
      if (!any) continue;
      if (lastIdx < text.length) {
        frag.appendChild(document.createTextNode(text.slice(lastIdx)));
      }
      node.parentNode.replaceChild(frag, node);
    }
    return tmp.innerHTML;
  }

  // Stream J quick fix (Phase 0): the prediction title slot historically
  // received the full prediction summary verbatim — so the panel showed
  // raw markdown (`**…**`), a redundant scope prefix `(Tech)` already
  // present in the badge above, and 140-char truncations like
  // `…spec…` instead of a real summary. Until the writer starts emitting
  // a dedicated `predictions.title` column (Stream J full), we clean the
  // displayed title at render time:
  //   1. drop leading scope prefix
  //   2. strip stray markdown asterisks
  //   3. fall back to first sentence + ellipsis when too long
  function cleanPredictionTitle(raw, fallbackFullText) {
    let s = String(raw == null ? "" : raw).trim();
    // Strip markdown asterisks FIRST — they wrap the scope prefix in
    // legacy entries (`**(Tech) …`), and the prefix-stripping regex
    // below anchors at `^\(`, so the asterisks would otherwise hide
    // the prefix. Order matters here.
    s = s.replace(/\*+/g, "").trim();
    // Multi-locale scope prefix to strip. Matches the same set the
    // backend parser's _PREFIX_TOKENS recognises so DB-resident
    // legacy data and freshly-rendered text both end up clean.
    //   EN:   Tech / Non-Tech / Non-tech / Business / Biz / Mix /
    //         Technical / Non-Technical / Technology
    //   JA:   技術 / 非技術 / 非-技術 / テクノロジー / 非テクノロジー /
    //         ビジネス / 非ビジネス / ビジ / ミックス
    //   ES:   Tecnología / Tecnologia / Tec / No-Tec /
    //         Técnico / Tecnico / Negocio
    //   FIL:  Teknikal / Hindi-Teknikal / Negosyo / Halong
    // Loops to clear accidental double-tags `(Mix) (Tech) …` and
    // catches both the full-paren shape `(Tech)` and the half-paren
    // shape `Tech)` left over by the legacy strip-set parser.
    const PREFIX = (
      "tech|non[-\\s]?tech|nontech|non[-\\s]?technical|nontechnical|" +
      "technical|technology|business|biz|mix|" +
      "技術|非技術|非[-\\s]?技術|テクノロジー|非テクノロジー|" +
      "ビジネス|非ビジネス|ビジ|ミックス|" +
      "tecnología|tecnologia|tec|no[-\\s]?tec|" +
      "técnico|tecnico|no[-\\s]?técnico|no[-\\s]?tecnico|" +
      "negocio|" +
      "teknikal|hindi[-\\s]?teknikal|negosyo|halo|halong"
    );
    const fullParen = new RegExp("^\\s*\\(\\s*(?:" + PREFIX + ")\\s*\\)\\s*", "i");
    const halfParen = new RegExp("^\\s*(?:" + PREFIX + ")\\s*\\)\\s*", "i");
    let prev = null;
    while (prev !== s) {
      prev = s;
      s = s.replace(fullParen, "");
      s = s.replace(halfParen, "");
    }
    // Collapse leftover whitespace.
    s = s.replace(/\s+/g, " ").trim();
    // Empty after cleaning → use fallback full text the same way.
    if (!s && fallbackFullText) {
      return cleanPredictionTitle(fallbackFullText, null);
    }
    // First-sentence fallback when the slot is overlong (≥ 80 chars).
    if (s.length >= 80) {
      // Match the first sentence terminator; allow common abbreviations
      // by requiring whitespace + uppercase or end-of-string after.
      const m = s.match(/^([^.!?]{8,160}?[.!?])(\s|$)/);
      if (m) {
        s = m[1].trim();
      } else {
        // No clean sentence break — hard-truncate and add an ellipsis.
        s = s.slice(0, 78).replace(/\s+\S*$/, "").trim() + "…";
      }
    }
    return s;
  }

  // Same whitelist as renderMarkdown, but only the inline pieces:
  // **bold**, *italic*, `code`. No links, no list expansion. Used
  // for short captions where block conversion would mangle layout.
  function renderMarkdownInline(s) {
    if (s == null) return "";
    let t = escapeHTML(String(s));
    t = t.replace(/`([^`]+?)`/g, '<code>$1</code>');
    t = t.replace(/\*\*([^*\n]+?)\*\*/g, '<strong>$1</strong>');
    t = t.replace(/(^|[\s(])\*([^*\s][^*\n]*?[^*\s]|[^*\s])\*(?=[\s).,!?:;]|$)/g, '$1<em>$2</em>');
    return annotateGlossary(t);
  }

  // Tiny markdown -> HTML for the panel's long-form text. Escapes
  // first, then turns a small whitelist of inline markers and a few
  // block patterns into HTML. Anything we don't recognize stays
  // literal — better than executing surprising input.
  function renderMarkdown(s) {
    if (s == null) return "";
    let t = escapeHTML(String(s));

    // Inline code spans: keep first so * inside code stays literal.
    t = t.replace(/`([^`]+?)`/g, '<code>$1</code>');
    // Links: [text](url) — url already HTML-escaped (& -> &amp; is fine in href)
    t = t.replace(
      /\[([^\]\n]+?)\]\((https?:\/\/[^\s)]+)\)/g,
      '<a href="$2" target="_blank" rel="noreferrer noopener">$1</a>',
    );
    // Bold ** ** (handle before single * for italics)
    t = t.replace(/\*\*([^*\n]+?)\*\*/g, '<strong>$1</strong>');
    // Italic * * (require non-space adjacent so multiplication stays literal)
    t = t.replace(/(^|[\s(])\*([^*\s][^*\n]*?[^*\s]|[^*\s])\*(?=[\s).,!?:;]|$)/g, '$1<em>$2</em>');

    // Blocks: split lines, group consecutive bullets into <ul>.
    const lines = t.split(/\r?\n/);
    const out = [];
    let inUL = false;
    for (const raw of lines) {
      const line = raw.trimEnd();
      const bullet = line.match(/^\s*[-*]\s+(.*)$/);
      if (bullet) {
        if (!inUL) { out.push("<ul>"); inUL = true; }
        out.push(`<li>${bullet[1]}</li>`);
      } else {
        if (inUL) { out.push("</ul>"); inUL = false; }
        if (line === "") out.push("");
        else out.push(line);
      }
    }
    if (inUL) out.push("</ul>");
    // Paragraph join: blank line splits paragraphs.
    const joined = out.join("\n").replace(/\n{2,}/g, "\n\n");
    const paras = joined.split(/\n\n/).map((p) => {
      const trimmed = p.trim();
      if (!trimmed) return "";
      if (/^<(ul|ol|li|h[1-6]|blockquote)/.test(trimmed)) return trimmed;
      return `<p>${trimmed.replace(/\n/g, "<br>")}</p>`;
    });
    return annotateGlossary(paras.filter(Boolean).join(""));
  }

