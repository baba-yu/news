  // Linear interpolate between HEAT_STOPS by t in [0,1]
  function heatColor(t) {
    t = clamp(t || 0, 0, 1);
    for (let i = 0; i < HEAT_STOPS.length - 1; i++) {
      const a = HEAT_STOPS[i], b = HEAT_STOPS[i + 1];
      if (t >= a.t && t <= b.t) {
        const f = (t - a.t) / Math.max(1e-6, b.t - a.t);
        return lerpColor(a.c, b.c, f);
      }
    }
    return HEAT_STOPS[HEAT_STOPS.length - 1].c;
  }
  function hexToRgb(hex) {
    const h = hex.replace("#", "");
    return {
      r: parseInt(h.slice(0, 2), 16),
      g: parseInt(h.slice(2, 4), 16),
      b: parseInt(h.slice(4, 6), 16),
    };
  }
  function lerpColor(a, b, t) {
    const ca = hexToRgb(a), cb = hexToRgb(b);
    const r = Math.round(ca.r + (cb.r - ca.r) * t);
    const g = Math.round(ca.g + (cb.g - ca.g) * t);
    const bl = Math.round(ca.b + (cb.b - ca.b) * t);
    return `rgb(${r},${g},${bl})`;
  }

  // Stable 0..1 hash so each node id gets the same z every reload.
  function hashUnit(s) {
    let h = 2166136261 >>> 0;
    const str = String(s || "");
    for (let i = 0; i < str.length; i++) {
      h ^= str.charCodeAt(i);
      h = Math.imul(h, 16777619);
    }
    return ((h >>> 0) % 10000) / 10000;
  }

  function zFor(node) {
    let range = Z_RANGE_BY_TYPE[node.type] || 40;
    // In MIX, force the two scopes onto orthogonal slabs:
    //   - TECH on the XY plane → squash z near 0
    //   - BIZ on the YZ plane → fan z out far so depth carries the spread
    // In single-scope views the per-type Z range stays as the natural
    // wobble that gives rotation something to reveal.
    if (state.scopeId === "mix" && node && node.scope_id) {
      if (node.scope_id === "tech") {
        range = Math.min(range, 18);
      } else if (node.scope_id === "business") {
        range = Math.max(range, 220);
      }
    }
    const t = hashUnit(node.id) * 2 - 1; // -1..1
    return t * range;
  }

  function radiusFor(node) {
    // Prefer our per-type config over layout.radius so updating RADIUS_BY_TYPE
    // actually changes visible size. Fall back to layout.radius only for
    // unknown node types, with a conservative cap.
    const byType = RADIUS_BY_TYPE[node.type];
    if (typeof byType === "number") return byType;
    const layoutR = node.layout && typeof node.layout.radius === "number" ? node.layout.radius : 10;
    return Math.min(layoutR, 14);
  }

