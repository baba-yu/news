  async function boot() {
    state.canvas = document.getElementById("graph-canvas");
    state.ctx = state.canvas.getContext("2d");
    resizeCanvas();

    // Restore persisted state (nice-to-have)
    const saved = loadPersisted();
    if (saved) {
      if (saved.scopeId) state.scopeId = saved.scopeId;
      if (saved.windowId) state.windowId = saved.windowId;
      if (saved.tool === "pan" || saved.tool === "rotate") state.tool = saved.tool;
      // Heat-metric selector was removed (only realization survives,
      // surfaced in the panel as "Hit rate"). Ignore any persisted
      // attention/grass preference so legacy users aren't stranded
      // with a metric they can't change anymore.
      state.heatMetric = "realization";
      if (typeof saved.locale === "string" && ["en","ja","es","fil"].includes(saved.locale)) {
        state.locale = saved.locale;
      }
      if (typeof saved.snapshotDate === "string" && /^\d{8}$/.test(saved.snapshotDate)) {
        state.snapshotDate = saved.snapshotDate;
      }
      if (Array.isArray(saved.hidden)) {
        state.hiddenCategories = new Set(saved.hidden);
      } else if (saved.hidden && typeof saved.hidden === "object") {
        // Legacy per-scope map → flatten to a single set so old sessions
        // that already had filters stay sensible after upgrade.
        const merged = new Set();
        for (const arr of Object.values(saved.hidden)) {
          if (Array.isArray(arr)) for (const id of arr) merged.add(id);
        }
        state.hiddenCategories = merged;
      }
    }

    try {
      await loadManifest();
      // Stream A: pull the active glossary so the first markdown
      // render already has hover tooltips wired in. Optional — fetch
      // failures are silently ignored, hover just won't fire.
      await loadGlossary();
      updateScopeButtons(state.scopeId);
      updateWindowButtons(state.windowId);
      updateToolButtons(state.tool);
      updateHeatButtons(state.heatMetric);
      updateLocaleButtons(state.locale);
      applyChromeStrings();
      // Snapshot index loads asynchronously; if a saved snapshot was
      // active on last visit, re-point DATA_DIR before the fetch.
      if (state.snapshotDate) {
        DATA_DIR = `${LIVE_DATA_DIR}/snapshots/${state.snapshotDate}`;
      }

      await loadScopeGraph(state.scopeId);
      rebuildCategoryFilters();
      rebuildSimulation();
      setStatus("");
      installEventHandlers();
      // Stream F + G (Phase 3): wire LIST/EVIDENCE view toggle.
      // GRAPH stays the default; clicking LIST/EVIDENCE swaps the canvas.
      state.view = state.view || "graph";
      attachAltViewHandlers();
      setView(state.view);
      // Populate the snapshot dropdown (gracefully no-ops if 404).
      loadSnapshotIndex();
      // Apply snap-mode styling if we restored a snapshot view from
      // localStorage on this session.
      const subEl = document.getElementById("meta-sub");
      if (subEl) subEl.classList.toggle("snap-mode", !!state.snapshotDate);
    } catch (err) {
      console.error(err);
      setStatus("failed to load data: " + (err && err.message ? err.message : err), true);
    }
  }

  // Start when DOM is ready
  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", boot);
  } else {
    boot();
  }
