  function renderStatusPills(m, n) {
    const pills = [];
    // Three orthogonal signals are pillared separately so they don't
    // mask each other:
    //   - Dormant (node.detail.dormant): pool-membership maintained by
    //     Task 4. Applies across all windows; emit a "dormant" pill in
    //     addition to whatever the per-window status says (so a node
    //     that's parked in the pool but still receiving Lukewarm pings
    //     in 30d shows "dormant · weakly_supported" together).
    //   - Hot / Lukewarm (m.status): relevance signal of evidence in
    //     the current window. Renders the pill text directly.
    //   - "new" pill: fired only when a prediction has truly never been
    //     validated — no grass_daily anywhere yet — and is also not in
    //     the dormant pool (a parked prediction is "new" only the first
    //     day it is generated; afterwards "dormant" is the right story).
    const isDormantNode = !!(n && n.detail && n.detail.dormant);
    const isNew = !hasEvidence(m) && !isDormantNode;
    if (isDormantNode) {
      pills.push(`<span class="pill">dormant · parked for revival</span>`);
    }
    if (isNew) {
      pills.push(`<span class="pill new">${localeStr("pill.new_not_validated")}</span>`);
    } else {
      if (m.status) pills.push(`<span class="pill">${m.status}</span>`);
      if (n && n.type !== "prediction") {
        // Aggregates — positive signal (how many summaries are
        // working) plus an optional weak count when it's meaningful.
        const supported = countSupportedDescendantPredictions(n);
        if (supported > 0) pills.push(`<span class="pill good">${supported} supported</span>`);
        const weak = countWeakDescendantPredictions(n);
        if (weak > 0) pills.push(`<span class="pill warn">${weak} weak</span>`);
        // Surface freshly-authored summaries on aggregates that have
        // already accumulated some validated children — those would
        // otherwise hide the "new" status entirely.
        const fresh = countNewDescendantPredictions(n);
        if (fresh > 0) pills.push(`<span class="pill new">${fresh} new</span>`);
      } else if (typeof m.realization_score === "number" && m.realization_score < WARN_T) {
        pills.push(`<span class="pill warn">${localeStr("pill.low_realization")}</span>`);
      }
    }
    // Contradiction axis is retired; pill only survives for legacy data
    // where the backend still emits a nonzero score.
    if (typeof m.contradiction_score === "number" && m.contradiction_score >= CONTRADICT_T) pills.push(`<span class="pill contradict">contradicted</span>`);
    return pills.join(" ");
  }

