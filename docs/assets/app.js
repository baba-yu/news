  function subtitleFor(n) {
    const parts = [];
    if (n.category_id) parts.push(n.category_id.split(".").pop());
    if (n.theme_id && n.theme_id !== n.id) parts.push(n.theme_id.split(".").pop());
    if (n.subtheme_id && n.subtheme_id !== n.id) parts.push(n.subtheme_id.split(".").pop());
    return parts.join(" / ");
  }

  function renderPanelBody(n) {
    const m = metricsFor(n, state.windowId);
    const pct = (v) => (typeof v === "number" ? (v * 100).toFixed(0) + "%" : "—");

    let extras = "";
    const detail = n.detail || {};

    // Common description — use the labels.* locale fan-out so JA / ES
    // / FIL sessions read in their language. detail.description is
    // EN-only on the export side; only fall back to it when nodeLabel
    // has nothing for the active locale.
    // For category / theme / subtheme nodes, `description` is a useful
    // standalone block (the theme's editorial description) and is
    // rendered right below the title. For *prediction* nodes,
    // `description` is the full long-form body, which is already
    // surfaced twice elsewhere — once via the Stream K mid-tier
    // summary (visible by default) and once via the collapsed
    // <details> full prediction text. Repeating it here would be a
    // third copy. Suppress for predictions so the panel reads:
    // title → mid-tier summary → tabs → meta → collapsed full body.
    const desc = (n.type === "prediction")
      ? ""
      : (nodeLabel(n, "description") || detail.description || "");
    const statusPills = renderStatusPills(m, n);

    // Metrics block. Order: Hit rate (left, primary "did the prediction
    // hit") then Attention (right, "how loud was the topic"). The old
    // "Daily level" tile was removed because it duplicated the Daily
    // Activity chart below.
    //   Hit rate   — mean observed relevance (1-5 → 0.2-1.0) across
    //                evidence in the window, weighted 0.65 new + 0.35
    //                continuing. Low = prediction is missing.
    //   Attention  — how much this theme/category was cited in the
    //                validation reports inside the window, weighted by
    //                relevance.
    const attTip = "How often this topic was cited (frequency × relevance) in the window";
    const realTip = "Weighted mean observed relevance (0.65 * new + 0.35 * continuing)";
    const mm = `
      <div class="metrics-grid">
        ${metricTile(localeStr("panel.realization"), m.realization_score, undefined, realTip)}
        ${metricTile(localeStr("panel.attention"),   m.attention_score,   undefined, attTip)}
      </div>
    `;

    // Activity chart (was: grass strip)
    const strip = renderActivityChart(m);

    // Type-specific
    if (n.type === "category") {
      const themes = (n.child_ids || []).map((id) => nodeById(id)).filter(Boolean);
      extras = `
        <h3>Child themes (${themes.length})</h3>
        <ul class="related-list">${themes.map(listItem).join("") || '<li class="muted">none</li>'}</ul>
        ${detail.active_theme_count != null ? `<p class="muted">active theme count: ${detail.active_theme_count}</p>` : ""}
      `;
    } else if (n.type === "theme") {
      const parentCat = n.parent_ids && n.parent_ids[0] ? nodeById(n.parent_ids[0]) : null;
      const preds = sortPredictionsByDateDesc(collectDescendantPredictions(n));
      extras = `
        ${parentCat ? `<h3>Parent category</h3><ul class="related-list">${listItem(parentCat)}</ul>` : ""}
        <h3>Predictions (${preds.length})</h3>
        <ul class="related-list">${preds.map((p) => listItem(p, { bare: true })).join("") || '<li class="muted">none</li>'}</ul>
        ${renderEvidenceSummary(detail)}
      `;
    } else if (n.type === "subtheme") {
      const preds = sortPredictionsByDateDesc(collectDescendantPredictions(n));
      extras = `
        <h3>Predictions (${preds.length})</h3>
        <ul class="related-list">${preds.map((p) => listItem(p, { bare: true })).join("") || '<li class="muted">none</li>'}</ul>
        ${renderEvidenceSummary(detail)}
      `;
    } else if (n.type === "prediction") {
      const parents = (n.parent_ids || []).map(nodeById).filter(Boolean);
      // For each parent, render a single "{CATEGORY} → {THEME}"
      // breadcrumb. The current data graph is 3-level
      // (category > theme > prediction) so a parent IS a theme — but
      // the loop also handles the legacy 4-level layout where the
      // parent is a subtheme by walking one step further.
      // Long names truncate via CSS ellipsis (.crumb).
      // Dedupe: cross-cut categorization can list the same theme via
      // multiple subtheme parents in legacy graphs.
      const seenTheme = new Set();
      const lineageRows = parents.map((p) => {
        const theme = p.type === "theme"
          ? p
          : ((p.parent_ids || []).map(nodeById).find((x) => x && x.type === "theme") || null);
        const cat = theme
          ? ((theme.parent_ids || []).map(nodeById).find((x) => x && x.type === "category") || null)
          : null;
        const themeKey = theme ? theme.id : `__no_theme__${p.id}`;
        if (seenTheme.has(themeKey)) return "";
        seenTheme.add(themeKey);
        const catLabel = cat
          ? (nodeLabel(cat, "short_label") || nodeLabel(cat, "label") || cat.id)
          : "—";
        const themeLabel = theme
          ? (nodeLabel(theme, "short_label") || nodeLabel(theme, "label") || theme.id)
          : "—";
        const targetId = (theme && theme.id) || p.id;
        return `<li class="lineage-row" data-goto="${escapeHTML(targetId)}">
          <span class="crumb crumb-cat" title="${escapeHTML(catLabel)}">${escapeHTML(catLabel)}</span>
          <span class="crumb-sep" aria-hidden="true">→</span>
          <span class="crumb crumb-theme" title="${escapeHTML(themeLabel)}">${escapeHTML(themeLabel)}</span>
        </li>`;
      }).join("");
      // Full-text prediction summary, locale-aware. The export emits
      // detail.prediction_summary_locales = { en, ja, es, fil }; the
      // top-level detail.prediction_summary is the EN string kept as
      // a fallback. Older field names (full_prediction_summary /
      // summary) are still read for snapshots that predate the rename.
      const summaryByLocale = (detail && detail.prediction_summary_locales) || {};
      const fullSummary = summaryByLocale[state.locale]
        || summaryByLocale.en
        || detail.prediction_summary
        || detail.full_prediction_summary
        || detail.summary
        || nodeLabel(n, "summary")
        || "";
      // 3-tier prediction pane: title → ELI14 (default visible,
      // 14-year-old level, ≤25 words) → collapsed full body.
      //
      //   Tier 1 (ELI14): the writer's plain-language one-sentence
      //     summary. Lives in `predictions.eli14` (Stream C). EN-only
      //     today; locale fan-out is a Phase X follow-up. This is
      //     the "what is this prediction in one breath" slot.
      //
      //   Tier 2 (Stream K Summary, optional secondary): a longer
      //     technical synopsis (≤ 300 chars). When present, rendered
      //     under the ELI14 in smaller text — the audience is the
      //     technical reader who wants more than ELI14 but doesn't
      //     want to expand the full body. Locale-aware.
      //
      //   Tier 3 (Full body): the long-form prose, collapsed in a
      //     <details>. Locale-aware. Auto-opens when neither ELI14
      //     nor Stream K is present (legacy items).
      //
      // Mid-tier slot priority for the visible-by-default text:
      //   ELI14 → Stream K Summary (locale-aware) → "" (empty).
      const reasoningLocales = (detail && detail.reasoning_locales) || {};
      const reasoningForMid = (detail && detail.reasoning) || {};
      const eli14Bag = reasoningLocales.eli14 || {};
      const _midLoc = state.locale || "en";
      const eli14Text = eli14Bag[_midLoc] || eli14Bag.en || reasoningForMid.eli14 || "";
      const midByLocale = (detail && detail.summary_short_locales) || {};
      const streamKSummary = midByLocale[state.locale]
        || midByLocale.en
        || detail.summary_short
        || "";
      // Whichever block has content drives the auto-open behavior of
      // the bottom <details> (open only when there's no mid-tier
      // content at all).
      const midSummary = eli14Text || streamKSummary;
      // Phase 3: prediction-level target window. Pulled from
      // `detail.target_start_date` / `target_end_date` (backfilled
      // from reasoning_landing). Renders below the mid-tier summary.
      const targetWindow = formatTargetWindow(
        detail.target_start_date, detail.target_end_date
      );
      // Stream I (Phase 2): four-tab right pane
      //   Reasoning  — Stream C reasoning_* + eli14
      //   Bridge     — Stream D validation-time bridge paragraphs
      //   Needs      — Stream E (driver-side actor + 5W1H task)
      //   Readings   — Stream F (cluster + chain + counter + relations)
      // Tabs are progressive: Reasoning is selected by default; the other
      // three are clickable. We render all four tab bodies inline (each
      // wrapped in a div the click handler toggles `hidden` on) so that
      // clicks don't have to re-render the whole panel.
      const tabsHtml = renderPredictionTabs(detail);
      extras = `
        ${eli14Text ? `
          <div class="prediction-eli14">${escapeHTML(eli14Text)}</div>
        ` : ""}
        ${streamKSummary ? `
          <details class="prediction-summary-mid${eli14Text ? "" : " open"}" ${eli14Text ? "" : "open"}>
            <summary class="prediction-summary-mid-toggle">${eli14Text ? localeStr("panel.summary.toggle.more") : localeStr("panel.summary.toggle.summary")}</summary>
            <div class="prediction-summary-mid-body md-body">${renderMarkdown(streamKSummary)}</div>
          </details>
        ` : ""}
        ${targetWindow ? `<p class="prediction-target-window"><span class="prediction-target-label">${localeStr("panel.lands")}</span> ${escapeHTML(targetWindow)}</p>` : ""}
        ${tabsHtml}
        ${detail.prediction_date ? `<p class="muted">prediction date: ${detail.prediction_date}</p>` : ""}
        ${lineageRows ? `<h3>${localeStr("panel.lineage")}</h3>
          <ul class="related-list lineage-list">${lineageRows}</ul>` : ""}
        ${detail.source_report_path ? `<h3>${localeStr("panel.source_report")}</h3><p>${repoLink(detail.source_report_path)}</p>` : ""}
        ${renderValidationReports(detail)}
        ${renderEvidenceLinks(detail.evidence || detail.evidence_links)}
        ${fullSummary ? `
          <details class="full-prediction" style="margin-top:14px"${midSummary ? "" : " open"}>
            <summary>${localeStr("panel.full_text")}</summary>
            <div class="md-body" style="margin-top:8px">${renderMarkdown(fullSummary)}</div>
          </details>` : ""}
      `;
    }

    const stripHeader = renderGrassStripHeader(m);
    return `
      <div>${statusPills}</div>
      ${desc ? `<div class="md-body" style="margin-top:10px">${renderMarkdown(desc)}</div>` : ""}
      <h3>Window · ${state.windowId}</h3>
      ${mm}
      <h3>${stripHeader}</h3>
      ${strip}
      ${extras}
    `;
  }

  // Stream I: four-tab right pane for prediction details. Reads
  // `detail.reasoning` (Stream C), `detail.bridges` (Stream D),
  // `detail.needs` (Stream E), and `detail.readings` (Stream F:
  // cluster density + frequency + chain + counter + P↔P relations).
  // The tab bodies are all rendered inline; click toggles the
  // `is-active` class on the body + tab. Empty sections fall back
  // to a contextual "no entry yet" message instead of a blank tab,
  // so legacy predictions don't show a void.
  function renderPredictionTabs(detail) {
    const reasoning = (detail && detail.reasoning) || {};
    // The Reasoning tab now shows because / given / so_that / landing
    // (the structural 4). ELI14 lives in the mid-tier slot above the
    // tabs, not here, so it doesn't count toward the tab's "has
    // content" check.
    const hasReasoning =
      reasoning.because || reasoning.given || reasoning.so_that
      || reasoning.landing;
    const bridges = (detail && detail.bridges) || [];
    const hasBridges = bridges.length > 0;
    const needs = (detail && detail.needs) || [];
    const hasNeeds = needs.length > 0;
    const readings = (detail && detail.readings) || {};
    const clusters = readings.clusters || [];
    const hasReadings = clusters.length > 0;

    const reasoningBody = hasReasoning
      ? renderReasoningTabBody(reasoning, detail)
      : `<p class="muted">${localeStr("panel.tab.reasoning.empty")}</p>`;
    const bridgeBody = hasBridges
      ? renderBridgesTabBody(bridges)
      : `<p class="muted">${localeStr("panel.tab.bridge.empty")}</p>`;
    const needsBody = hasNeeds
      ? renderNeedsTabBody(needs)
      : `<p class="muted">${localeStr("panel.tab.needs.empty")}</p>`;
    const readingsBody = hasReadings
      ? renderReadingsTabBody(readings)
      : `<p class="muted">${localeStr("panel.tab.readings.empty")}</p>`;

    return `
      <div class="prediction-tabs" role="tablist" aria-label="Prediction details">
        <button class="ptab is-active" data-tab="reasoning" role="tab" aria-selected="true">${localeStr("panel.tab.reasoning")}</button>
        <button class="ptab" data-tab="bridge"   role="tab" aria-selected="false">${localeStr("panel.tab.bridge")}${hasBridges ? ` <span class="ptab-count">(${bridges.length})</span>` : ""}</button>
        <button class="ptab" data-tab="needs"    role="tab" aria-selected="false">${localeStr("panel.tab.needs")}${hasNeeds ? ` <span class="ptab-count">(${needs.length})</span>` : ""}</button>
        <button class="ptab" data-tab="readings" role="tab" aria-selected="false">${localeStr("panel.tab.readings")}${hasReadings ? ` <span class="ptab-count">(${clusters.length})</span>` : ""}</button>
      </div>
      <div class="prediction-tab-bodies">
        <div class="ptab-body is-active" data-tab-body="reasoning" role="tabpanel">${reasoningBody}</div>
        <div class="ptab-body" data-tab-body="bridge" role="tabpanel" hidden>${bridgeBody}</div>
        <div class="ptab-body" data-tab-body="needs" role="tabpanel" hidden>${needsBody}</div>
        <div class="ptab-body" data-tab-body="readings" role="tabpanel" hidden>${readingsBody}</div>
      </div>
    `;
  }

