# Public renderer
# ---------------------------------------------------------------------------


def render_day(repo_root, date_iso: str, locale: str = "en") -> str:
    """Assemble and return the rendered markdown for a single date.

    Does not write to disk. Use :func:`render_and_write` for the writer
    flow that also runs ``post-write-integrity``.
    """
    repo_root = Path(repo_root)
    env = _build_env()
    template = env.get_template("news.md.j2")

    data = _load_jsons(repo_root, date_iso, locale)
    ctx = {
        "date_iso": date_iso,
        "date_compact": date_iso.replace("-", ""),
        "coverage_window": _coverage_window(date_iso),
        "locale": locale,
        "ai_notice_block": _ai_notice(locale),
        **data,
    }
    rendered = template.render(**ctx)
    # Normalize line endings + trim runs of >2 blank lines that Jinja2
    # whitespace control sometimes leaves behind.
    rendered = rendered.replace("\r\n", "\n").replace("\r", "\n")
    while "\n\n\n\n" in rendered:
        rendered = rendered.replace("\n\n\n\n", "\n\n\n")
    if not rendered.endswith("\n"):
        rendered += "\n"
    return rendered


def output_path(repo_root, date_iso: str, locale: str) -> Path:
    """Return the canonical output path for a (date, locale) pair."""
    repo_root = Path(repo_root)
    return (
        repo_root
        / "report"
        / locale
        / f"news-{date_iso.replace('-', '')}.md"
    )


def _write_atomic(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp = tempfile.mkstemp(prefix=path.name + ".", dir=str(path.parent))
    try:
        with os.fdopen(fd, "w", encoding="utf-8", newline="\n") as f:
            f.write(text)
        os.replace(tmp, path)
    except Exception:
        try:
            os.remove(tmp)
        except OSError:
            pass
        raise


def render_and_write(repo_root, date_iso: str, locale: str = "en") -> Path:
    """Render + atomically write + run post-write-integrity (kind=news).

    On post-write-integrity failure, the markdown stays on disk so the
    caller can inspect it; the function raises ``RuntimeError`` with
    the integrity report as the message. The write is atomic — if
    rendering fails, the existing file (if any) is untouched.
    """
    rendered = render_day(repo_root, date_iso, locale)
    out = output_path(repo_root, date_iso, locale)
    _write_atomic(out, rendered)
    proc = subprocess.run(
        [
            sys.executable,
            "-m",
            "app.skills.post_write_integrity",
            "--kind",
            "news",
            "--path",
            str(out),
        ],
        cwd=str(Path(repo_root)),
        capture_output=True,
        text=True,
    )
    if proc.returncode != 0:
        raise RuntimeError(
            f"post-write-integrity failed for {out}:\n"
            f"{proc.stdout}\n{proc.stderr}"
        )
    return out


def main(argv: list[str] | None = None) -> int:
    import argparse

    p = argparse.ArgumentParser(
        description="Render a news report from sourcedata JSON."
    )
    p.add_argument("--date", required=True, help="ISO date, e.g. 2026-05-04")
    p.add_argument("--locale", default="en", choices=["en", "ja", "es", "fil"])
    p.add_argument(
        "--repo-root",
        default=".",
        help="Repository root (defaults to cwd).",
    )
    p.add_argument(
        "--write",
        action="store_true",
        help="Write to report/<locale>/news-YYYYMMDD.md (else stdout).",
    )
    args = p.parse_args(argv)

    if args.write:
        out = render_and_write(args.repo_root, args.date, args.locale)
        print(f"OK wrote {out}")
        return 0
    print(render_day(args.repo_root, args.date, args.locale), end="")
    return 0


if __name__ == "__main__":  # pragma: no cover
    sys.exit(main())
