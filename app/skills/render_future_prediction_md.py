"""Deterministic Jinja2 renderer for
``future-prediction/<locale>/future-prediction-YYYYMMDD.md``.

Spec: ``design/sourcedata-layout.md §Skill decomposition`` and
``§Markdown rendered shape (after)``.

Reads the per-date sourcedata JSON files
(``app/sourcedata/<date>/bridges.json`` plus optional ``summary.json``,
plus the locale variants under ``app/sourcedata/locales/<date>/<locale>/``)
and returns/writes the rendered markdown. The renderer produces clean
prose: no ``Pred ID #N``, no ``Coherence N/5``, no ``Remaining gap:``
parser anchors — those internal field names live in the JSON only.

Public API:

  * :func:`render_day(repo_root, date_iso, locale) -> str` — assemble.
  * :func:`render_and_write(repo_root, date_iso, locale) -> Path` —
    atomic write + post-write-integrity.

Soft-imports ``jinja2``: see render_news_md.py for rationale.
"""

from __future__ import annotations

import os
import subprocess
import sys
import tempfile
from pathlib import Path

from app.skills.ingest_sourcedata import date_dir, locale_date_dir
from app.skills.sourcedata_schemas import BridgesFile


_AI_NOTICE = {
    "en": (
        "<!-- ai-notice -->\n"
        "> **Note:** The articles and summaries on this page are "
        "generated by Anthropic's Claude (a generative AI)."
    ),
    "ja": (
        "<!-- ai-notice -->\n"
        "> **ご注意:** 本ページの記事および要約は、Anthropic 社の生成AI"
        "「Claude」によって作成されています。"
    ),
    "es": (
        "<!-- ai-notice -->\n"
        "> **Nota:** Los artículos y resúmenes de esta página son "
        "generados por Claude (IA generativa) de Anthropic."
    ),
    "fil": (
        "<!-- ai-notice -->\n"
        "> **Paalala:** Ang mga artikulo at buod sa pahinang ito ay "
        "nilikha ng generative AI na Claude ng Anthropic.\n"
    ),
}


def _ai_notice(locale: str) -> str:
    return _AI_NOTICE.get(locale, _AI_NOTICE["en"])


def _coverage_window(date_iso: str) -> str:
    """7-day rolling window stanza for the FP-report header.

    Mirrors the existing legacy header phrasing but written as prose
    rather than relying on inline backticks (which the lint check flags
    as a parser anchor surface).
    """
    import datetime as _dt

    d = _dt.date.fromisoformat(date_iso)
    start = (d - _dt.timedelta(days=7)).isoformat()
    end = (d - _dt.timedelta(days=1)).isoformat()
    return (
        f"predictions from {start} through {end} (last 7 days, "
        f"excluding today). Today's news report is for {date_iso}."
    )


def _read_json(path: Path) -> dict:
    import json

    return json.loads(path.read_text(encoding="utf-8"))


def _load_summary(base: Path) -> dict | None:
    """Optional ``summary.json`` carrying the three trailing prose blocks.

    Schema (all keys optional):

        {
          "plain_language":       "<short prose>",
          "findings":             "<multi-paragraph prose>",
          "relation_to_my_preds": "<multi-paragraph prose>"
        }

    The bridges.json schema does NOT carry these because they're report-
    level rather than per-validation-row. Spec
    `design/sourcedata-layout.md §Markdown rendered shape (after)` says
    we may extend ``bridges.json`` OR add a sibling file — sibling
    chosen here because the three fields are independent of the
    per-row bridge data and re-validating bridges.json on every
    summary edit would be wasteful.
    """
    sp = base / "summary.json"
    if not sp.is_file():
        return None
    raw = _read_json(sp)
    if not isinstance(raw, dict):
        return None
    out: dict = {}
    for key in ("plain_language", "findings", "relation_to_my_preds"):
        v = raw.get(key)
        if isinstance(v, str) and v.strip():
            out[key] = v
    return out or None


def _load_jsons(repo_root: Path, date_iso: str, locale: str) -> dict:
    if locale == "en":
        base = date_dir(repo_root, date_iso)
    else:
        base = locale_date_dir(repo_root, date_iso, locale)

    bridges_path = base / "bridges.json"
    if bridges_path.is_file():
        bridges = BridgesFile.from_dict(_read_json(bridges_path)).to_dict()
    else:
        bridges = {"date": date_iso, "validation_rows": []}

    summary = _load_summary(base)
    return {"bridges": bridges, "summary": summary}


# ---------------------------------------------------------------------------
# Jinja2 environment + filters
# ---------------------------------------------------------------------------


def _build_env():
    try:
        import jinja2
    except ImportError as e:  # pragma: no cover
        raise RuntimeError(
            "render_future_prediction_md requires jinja2. Install via "
            "`pip install jinja2`."
        ) from e

    template_dir = Path(__file__).resolve().parents[1] / "templates"
    env = jinja2.Environment(
        loader=jinja2.FileSystemLoader(str(template_dir)),
        autoescape=False,
        keep_trailing_newline=True,
    )
    env.filters["render_citations"] = _render_citations
    env.filters["table_cell"] = _table_cell
    return env


def _render_citations(citations) -> str:
    if not citations:
        return ""
    parts = []
    for c in citations:
        label = c.get("label", "") if isinstance(c, dict) else ""
        url = c.get("url", "") if isinstance(c, dict) else ""
        parts.append(f"[{label}]({url})")
    return ", ".join(parts)


def _table_cell(text) -> str:
    if text is None:
        return ""
    return str(text).replace("\n", " ").replace("|", "\\|").strip()


# ---------------------------------------------------------------------------
# Public renderer
# ---------------------------------------------------------------------------


def render_day(repo_root, date_iso: str, locale: str = "en") -> str:
    repo_root = Path(repo_root)
    env = _build_env()
    template = env.get_template("future_prediction.md.j2")

    data = _load_jsons(repo_root, date_iso, locale)
    ctx = {
        "date_iso": date_iso,
        "coverage_window": _coverage_window(date_iso),
        "locale": locale,
        "ai_notice_block": _ai_notice(locale),
        **data,
    }
    rendered = template.render(**ctx)
    rendered = rendered.replace("\r\n", "\n").replace("\r", "\n")
    while "\n\n\n\n" in rendered:
        rendered = rendered.replace("\n\n\n\n", "\n\n\n")
    if not rendered.endswith("\n"):
        rendered += "\n"
    return rendered


def output_path(repo_root, date_iso: str, locale: str) -> Path:
    repo_root = Path(repo_root)
    return (
        repo_root
        / "future-prediction"
        / locale
        / f"future-prediction-{date_iso.replace('-', '')}.md"
    )


def _write_atomic(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp = tempfile.mkstemp(prefix=path.name + ".", dir=str(path.parent))
    try:
        with os.fdopen(fd, "w", encoding="utf-8", newline="\n") as f:
            f.write(text)
        os.replace(tmp, path)
    except Exception:
        try:
            os.remove(tmp)
        except OSError:
            pass
        raise


def render_and_write(repo_root, date_iso: str, locale: str = "en") -> Path:
    rendered = render_day(repo_root, date_iso, locale)
    out = output_path(repo_root, date_iso, locale)
    _write_atomic(out, rendered)
    proc = subprocess.run(
        [
            sys.executable,
            "-m",
            "app.skills.post_write_integrity",
            "--kind",
            "future-prediction",
            "--path",
            str(out),
        ],
        cwd=str(Path(repo_root)),
        capture_output=True,
        text=True,
    )
    if proc.returncode != 0:
        raise RuntimeError(
            f"post-write-integrity failed for {out}:\n"
            f"{proc.stdout}\n{proc.stderr}"
        )
    return out


def main(argv: list[str] | None = None) -> int:
    import argparse

    p = argparse.ArgumentParser(
        description="Render a future-prediction report from sourcedata JSON."
    )
    p.add_argument("--date", required=True, help="ISO date, e.g. 2026-05-04")
    p.add_argument("--locale", default="en", choices=["en", "ja", "es", "fil"])
    p.add_argument("--repo-root", default=".")
    p.add_argument("--write", action="store_true")
    args = p.parse_args(argv)

    if args.write:
        out = render_and_write(args.repo_root, args.date, args.locale)
        print(f"OK wrote {out}")
        return 0
    print(render_day(args.repo_root, args.date, args.locale), end="")
    return 0


if __name__ == "__main__":  # pragma: no cover
    sys.exit(main())
